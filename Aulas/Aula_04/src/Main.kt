/*
Operadores Aritiméticos

+ = Soma
- = Subtração
* = Multiplicação
/ = Divisâo
% = Módulo/Resto da Divisão
++ = Incremento
-- = Decremento

 */

fun main() {
    var sum = 10 + 10
    var sub = 50 - 10
    var mul = 10 * 10
    var div = 100 / 10
    var mod = 100 / 3

    println("A soma é: $sum")
    println("A subtração é: $sub")
    println("A multiplicação é: $mul")
    println("A divisão é: $div")
    println("O resto da divisão é: $mod")
}