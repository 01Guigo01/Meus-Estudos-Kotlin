/*

Operadores Relacionais

== -  Igual
< - Menor
<= - Menor ou Igual
> - Maior
>= - Maior ou Igual
!= - Diferente

 */

fun main() {
    var num_1 = 10
    var num_2 = 100

    println("A variável num_1 é igual a num_2?: ${num_1 == num_2}")
    println("A variável num_1 é menor a num_2?: ${num_1 < num_2}")
    println("A variável num_1 é menor ou igual a num_2?: ${num_1 <= num_2}")
    println("A variável num_1 é maior a num_2?: ${num_1 > num_2}")
    println("A variável num_1 é maior ou igual a num_2?: ${num_1 >= num_2}")
    println("A variável num_1 é diferente a num_2?: ${num_1 >= num_2}")
}